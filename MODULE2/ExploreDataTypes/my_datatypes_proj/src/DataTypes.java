public class DataTypes {
    public static void main(String[] args) {
        byte b = 125;
        System.out.println("Byte value: " + b);
        short points = 3000;
        System.out.println("Short value: " + points);

        int population = 4358000;
        System.out.println("Integer value: " + population);

        long age_in_milliseconds = 1234567890L;
        System.out.println("Long value: " + age_in_milliseconds);

        char keyboard_input = 'a';
        System.out.println("Char value: " + keyboard_input);

        float house_value = 400000.58f;
        System.out.println("House value: $" + house_value);

        double pi = 3.141592653589793;
        System.out.println("Value of Pi: " + pi);

    }
}